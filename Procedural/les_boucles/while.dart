//Les boucles permettent d'exécuter un bloc de code plusieurs fois,
// en fonction d'une condition.
// En Dart, les boucles les plus courantes sont les suivantes:
// for
//while: Utilisé pour itérer tant qu'une condition est vraie.

void main() {
  int x = 1;
  while (x != 5){
    print(x);
    x++;
}
print("La valeur final de x est $x");
  
}